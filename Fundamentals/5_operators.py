#  Operators 
# 1. Arithmetic Operator 

a = 9
b = 4
   
add = a + b 
sub = a - b 
mul = a * b 
div1 = a / b 
div2 = a // b 
mod = a % b 
   
# print results 
print(add) 
print(sub) 
print(mul) 
print(div1) 
print(div2) 
print(mod)

# Relational Operators 
print(a > b)    
print(a < b) 
print(a == b) 
print(a != b) 
print(a >= b) 
print(a <= b)